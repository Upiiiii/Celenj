void main(){
    // !=
    int num1 = 10;
    int num2 = 5;

    if(num1 != num2){
        print('true');
    }else{
        print('false');
    }

    // >
    int num3 = 8;
    int num4 = 6;

    if(num3 > num4){
        print('true');
    }else{
        print('false');
    }

    // <

    // ==
    int num5 = 6;
    int num6 = 6;

    if(num5 == num6){
        print('true');
    }else{
        print('false');
    }

    // >=
    int num7 = 6;
    int num8 = 6;

    if(num7 >= num8){
        print('true');
    }else{
        print('false');
    }

    // <=

    int num9 = 6;
    int num10 = 6;

    if(num9 <= num10){
        print('true');
    }else{
        print('false');
    }
}