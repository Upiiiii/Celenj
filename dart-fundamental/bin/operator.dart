void main(){
    // +
    int num1 = 3;
    int num2 = 8;

    var result = num1 + num2;
    print(result);

    // -
    int num3 = 9;
    int num4 = 7;

    var result1 = num3 - num4;
    print(result1);

    // *
    int num5 = 10;
    int num6 = 5;

    var result2 = num5 * num6;
    print(result2);

    // /
    int num7 = 2;
    int num8 = 8;

    var result3 = num7 / num8;
    print(result3);
}